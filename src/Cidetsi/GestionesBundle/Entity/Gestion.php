<?php

namespace Cidetsi\GestionesBundle\Entity;

class Gestion
{
    private $id;
    private $name;
    private $status;

    public function getId() {
        return $this->id;
    }

    public function setName($name) {
        $this->name = $name;
        return $this;
    }

    public function getName() {
        return $this->name;
    }

    public function setStatus($status) {
        $this->status = $status;
        return $this;
    }

    public function getStatus() {
        return $this->status;
    }
}